def force_qgis_python_exe():
    """
    Ensure that any subprocess/multiprocessing spawned from within QGIS uses the
    bundled python.exe, not qgis-ltr-bin.exe (which would launch another QGIS instance).
    """
    try:
        # In QGIS, sys.exec_prefix usually points to .../apps/Python312
        py = os.path.join(sys.exec_prefix, "python.exe")
        if os.path.exists(py):
            # Make libraries that call subprocess via sys.executable use python.exe
            sys.executable = py

            # Make multiprocessing spawn use python.exe
            mp.set_executable(py)

            # Helps on Windows embedded/frozen-ish contexts
            mp.freeze_support()
    except Exception:
        pass

force_qgis_python_exe()



